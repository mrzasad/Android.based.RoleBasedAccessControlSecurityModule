package com.android.rolebaseaccesscontrollibrary.models;

public class WebPage {

    private int id;
    private String url;

    public WebPage(int id, String url) {
        this.id = id;
        this.url = url;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
